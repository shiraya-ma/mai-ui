// useMaiSnsLink
'use client';
import type { MaiSNSLinkPresenter } from "../../mai-sns-link-presenter";

export function useMaiSNSLink (props: MaiSNSLinkPresenter.Props) {
    const {
        className,
        size,
        ...btnProps
    } = props;

    const byColor = props.color === 'default'  || !props.color? 'border-[var(--nextui-foreground)]': undefined;

    const bySize =
    size === 'sm'? '!size-8 [&>span]:text-xl':
    size === 'md'? '!size-12 [&>span]:text-2xl':
    '!size-16 [&>span]:text-3xl'

    const _className = [
        byColor,
        bySize,
        'flex gap-2 justify-center border rounded-full !min-w-0 !p-1/5',
        '[&>svg]:w-auto [&>svg]:h-3/5 [&>svg]:aspect-square',
        'data-[with-text=true]:!w-auto',
        className
    ].filter(t => typeof t === 'string').join(' ');

    return {
        className: _className,
        ...btnProps
    };
};
